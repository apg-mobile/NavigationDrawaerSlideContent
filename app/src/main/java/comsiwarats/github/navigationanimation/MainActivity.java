    private static final float minimumOffset = 0.75f;
    private static final float maximumOffset = 1.0f;

    @Override
    public void onDrawerSlide(View drawerView, float x) {
        float offset = minimumOffset / (x + ((maximumOffset - x) * minimumOffset));
        content.setScaleX(offset);
        content.setScaleY(offset);
        content.setTranslationX(drawerView.getWidth() * x);
    }

